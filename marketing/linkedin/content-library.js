// Helper Functions for Dynamic Content

window.getRandomValueProp = function(category = 'primary') {
    const props = window.contentLibrary.valueProps[category];
    return props[Math.floor(Math.random() * props.length)];
};

window.getRandomStat = function(category = 'platform') {
    const stats = window.contentLibrary.statistics[category];
    const keys = Object.keys(stats);
    const randomKey = keys[Math.floor(Math.random() * keys.length)];
    return stats[randomKey];
};

window.getRandomCTA = function(type = 'standard') {
    const ctas = window.contentLibrary.ctas[type];
    return ctas[Math.floor(Math.random() * ctas.length)];
};

window.getPainSolutionPair = function() {
    const painPoints = window.contentLibrary.painPoints;
    const keys = Object.keys(painPoints);
    const randomKey = keys[Math.floor(Math.random() * keys.length)];
    return painPoints[randomKey];
};

window.getSeasonalHook = function() {
    const month = new Date().getMonth();
    const seasons = window.contentLibrary.seasonal;
    
    if (month >= 2 && month <= 4) return seasons.spring;
    if (month >= 5 && month <= 7) return seasons.summer;
    if (month >= 8 && month <= 10) return seasons.fall;
    if (month === 11) return seasons.yearEnd;
    if (month === 0) return seasons.newYear;
    return seasons.winter;
};

window.getHashtagSet = function(type = 'primary', count = 10) {
    const allHashtags = [];
    
    // Always include primary hashtags
    allHashtags.push(...window.contentLibrary.hashtags.primary);
    
    // Add type-specific hashtags
    if (type !== 'primary' && window.contentLibrary.hashtags[type]) {
        allHashtags.push(...window.contentLibrary.hashtags[type]);
    }
    
    // Add some trending hashtags
    const trending = window.contentLibrary.hashtags.trending;
    allHashtags.push(...trending.slice(0, 2));
    
    // Remove duplicates and limit count
    return [...new Set(allHashtags)].slice(0, count);
};

// Dynamic content generation helpers
window.generateDynamicContent = function(template) {
    // Replace dynamic placeholders in templates
    let content = template;
    
    // Replace {{STAT}} with random statistic
    content = content.replace(/{{STAT}}/g, () => window.getRandomStat('performance'));
    
    // Replace {{USERS}} with user count
    content = content.replace(/{{USERS}}/g, window.contentLibrary.statistics.platform.activeUsers);
    
    // Replace {{SAVING}} with time saving
    content = content.replace(/{{SAVING}}/g, window.contentLibrary.statistics.platform.timeSaved);
    
    // Replace {{CTA}} with random CTA
    content = content.replace(/{{CTA}}/g, () => window.getRandomCTA());
    
    return content;
};


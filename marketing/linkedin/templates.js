// Template selection helper functions
window.getTemplateCategories = function() {
    const categories = new Set();
    Object.keys(window.linkedInTemplates).forEach(key => {
        const [category] = key.split('_');
        categories.add(category);
    });
    return Array.from(categories);
};

window.getRandomTemplate = function(category, tone) {
    const key = `${category}_${tone}`;
    const templates = window.linkedInTemplates[key];
    
    if (!templates) {
        // Fallback to any template in the category
        const fallbackKey = Object.keys(window.linkedInTemplates).find(k => k.startsWith(category));
        return window.linkedInTemplates[fallbackKey]?.[0] || window.linkedInTemplates['ai_advantage_professional'][0];
    }
    
    if (Array.isArray(templates)) {
        return templates[Math.floor(Math.random() * templates.length)];
    }
    
    return templates;
};

// Export template statistics
window.templateStats = {
    totalTemplates: Object.values(window.linkedInTemplates).reduce((acc, val) => {
        return acc + (Array.isArray(val) ? val.length : 1);
    }, 0),
    categories: window.getTemplateCategories().length,
    tones: ['professional', 'friendly', 'urgent', 'storytelling'].length
};
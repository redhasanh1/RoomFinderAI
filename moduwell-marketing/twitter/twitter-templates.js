// Template selection helpers
window.getRandomTemplate = function(category) {
    const templates = window.twitterTemplates[category];
    if (!templates) return '';
    return templates[Math.floor(Math.random() * templates.length)];
};

window.getRandomHook = function(type) {
    const hooks = window.hookGenerators[type];
    if (!hooks) return '';
    return hooks[Math.floor(Math.random() * hooks.length)];
};

window.getRandomCTA = function(audience = 'general') {
    const ctas = window.ctaTemplates[audience] || window.ctaTemplates.general;
    return ctas[Math.floor(Math.random() * ctas.length)];
};
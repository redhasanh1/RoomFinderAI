// Template Selection Helpers
window.getAuthorityTemplate = function(focus, style) {
    const templates = window.authorityTemplates[focus];
    if (!templates) return getDefaultAuthorityTemplate(focus);
    
    if (templates[style]) {
        const options = Array.isArray(templates[style]) ? templates[style] : [templates[style]];
        return options[Math.floor(Math.random() * options.length)];
    }
    
    // Get random from category
    const keys = Object.keys(templates);
    const randomKey = keys[Math.floor(Math.random() * keys.length)];
    const options = Array.isArray(templates[randomKey]) ? templates[randomKey] : [templates[randomKey]];
    return options[Math.floor(Math.random() * options.length)];
};

window.getViralHook = function(type = 'breakthrough') {
    const hooks = window.viralHooks[type] || window.viralHooks.breakthrough;
    return hooks[Math.floor(Math.random() * hooks.length)];
};

